bool lookup_constant(std::string_view name, double* out_value) {
    auto it = CONSTANTS.find(std::string(name));
    if (it == CONSTANTS.end())
        return false;
    if (out_value)
        *out_value = it->second;
    return true;
}

bool lookup_builtin_var(std::string_view name, BuiltinVar* out_info) {
    auto it = VARIABLES.find(std::string(name));
    if (it == VARIABLES.end())
        return false;
    if (out_info) {
        out_info->is_global = (it->second & 0x01) != 0;
        out_info->is_automatic_array = (it->second & 0x02) != 0;
        out_info->can_set = (it->second & 0x04) != 0;
    }
    return true;
}

bool lookup_builtin_func(std::string_view name) {
    return FUNCTIONS.find(std::string(name)) != FUNCTIONS.end();
}

